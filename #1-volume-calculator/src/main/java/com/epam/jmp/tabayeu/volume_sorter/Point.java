package com.epam.jmp.tabayeu.volume_sorter;

/**
 * So simple that we even don't use getters\setters here.
 * @author Tsimur_Abayeu
 * Nov 28, 2015
 */
public class Point {

	public float x;

	public float y;

	public float z;

	/**
	 * @param x
	 * @param y
	 * @param z
	 */
	public Point(float x, float y, float z) {
		super();
		this.x = x;
		this.y = y;
		this.z = z;
	}
}
